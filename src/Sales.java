package src;

public class Sales {

  private int month;
  private int year;
  private float value;

  public int getMonth() {
    return month;
  }

  public int setMonth(int month) {
    return this.month = month;
  }

  public int getYear() {
    return year;
  }

  public int setYear(int year) {
    return this.year = year;
  }

  public float getValue() {
    return value;
  }

  public float setValue(float value) {
    return this.value = value;
  }
}
